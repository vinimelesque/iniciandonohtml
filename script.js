// Declaração de variável

num1 = (parseInt(prompt('Digite o primeiro número: ')));
// parseInt -> transforma uma string em um número inteiro.
// string -> caractere.

num2 = (parseInt(prompt('Digite o segundo número: ')));

soma = (num1 + num2); // soma dos números.

document.writeln('<h1>' +soma+ '<h1>'); // imprime a soma dos dois números na tela.
//document.writeln -> Grava uma seqüência de texto, seguido por um caractere de nova linha a um documento.